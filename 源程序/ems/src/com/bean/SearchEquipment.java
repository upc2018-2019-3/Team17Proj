package com.bean;

public class SearchEquipment {
	
String sname;
	
	String scode;
	
	float eworth;
	
	int ecount;
	
	int sid;

	public int getEcount() {
		return ecount;
	}

	public void setEcount(int ecount) {
		this.ecount = ecount;
	}

	public float getEworth() {
		return eworth;
	}

	public void setEworth(float eworth) {
		this.eworth = eworth;
	}

	public String getScode() {
		return scode;
	}

	public void setScode(String scode) {
		this.scode = scode;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}
	
	

}
