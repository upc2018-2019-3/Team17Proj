package com.bean;

public class Reject {
	
	int eid;
	
	int rid;

	String rdate;
	
	float rdepreciation;
	
	String rremarks;
	
	String ename;
	
	String ecode;
	
	
	public int getEid() {
		return eid;
	}

	public void setEid(int eid) {
		this.eid = eid;
	}

	public String getRdate() {
		return rdate;
	}

	public void setRdate(String rdate) {
		this.rdate = rdate;
	}

	public float getRdepreciation() {
		return rdepreciation;
	}

	public void setRdepreciation(float rdepreciation) {
		this.rdepreciation = rdepreciation;
	}

	public int getRid() {
		return rid;
	}

	public void setRid(int rid) {
		this.rid = rid;
	}

	public String getRremarks() {
		return rremarks;
	}

	public void setRremarks(String rremarks) {
		this.rremarks = rremarks;
	}

	public String getEcode() {
		return ecode;
	}

	public void setEcode(String ecode) {
		this.ecode = ecode;
	}

	public String getEname() {
		return ename;
	}

	public void setEname(String ename) {
		this.ename = ename;
	}

	

}
